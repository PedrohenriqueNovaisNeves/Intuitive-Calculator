    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (retangulo.validaRetangulo(Double.parseDouble(jTextField1.getText()), Double.parseDouble(jTextField2.getText()))) {
                jPanel1.setVisible(true);
                jLabel7.setText("Valores negativos nao podem ser utilizados para calcular areas");
                jLabel8.setText("");
                jLabel9.setText("");
            }else{
            retangulo.retangulo(Double.parseDouble(jTextField1.getText()), Double.parseDouble(jTextField2.getText()));
            jPanel1.setVisible(false);
            jLabel6.setText(Double.toString(retangulo.getResultadoRetangulo()));
            }
        } catch (NumberFormatException e) {
            jPanel1.setVisible(true);
            jLabel8.setText("Certifique-se de que ambos os campos estao preenchidos");
            jLabel9.setText("Simbolos e letras nao sao aceitos");
            jLabel7.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


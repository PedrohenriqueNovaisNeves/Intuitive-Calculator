    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            if(triangulo.validaTriangulo(Double.parseDouble(jTextField1.getText()), Double.parseDouble(jTextField2.getText()), Double.parseDouble(jTextField3.getText()))){
                jPanel1.setVisible(true);
                jLabel8.setText("Valores negativos nao podem ser utilizados para calcular areas");
                jLabel9.setText("");
                jLabel10.setText("");
            }else{
                jPanel1.setVisible(false);
                triangulo.triangulo(Double.parseDouble(jTextField1.getText()), Double.parseDouble(jTextField2.getText()), Double.parseDouble(jTextField3.getText()));
                jLabel7.setText(Double.toString(triangulo.getResultadoTriangulo()));
            }
        }catch(NumberFormatException e){
              jPanel1.setVisible(true);
              jLabel9.setText("Certifique-se de que ambos os campos estao preenchidos");
              jLabel10.setText("Simbolos e letras nao sao aceitos");
              jLabel8.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


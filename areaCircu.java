    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            if(circulo.validaCirculo(Double.parseDouble(jTextField1.getText()))){
                jPanel1.setVisible(true);
                jLabel4.setText("Valores negativos nao podem ser utilizados para calcular areas");
                jLabel5.setText("");
                jLabel6.setText("");
            }else{
                jPanel1.setVisible(false);
                circulo.circulo(Double.parseDouble(jTextField1.getText()));
                jLabel8.setText(Double.toString(circulo.getResultadoCirculo()));
            }
        }catch(NumberFormatException e){
            jPanel1.setVisible(true);
            jLabel5.setText("Certifique-se de que ambos os campos estao preenchidos");
            jLabel6.setText("Simbolos e letras nao sao aceitos");
            jLabel4.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

